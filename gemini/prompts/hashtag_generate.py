# -*- coding: utf-8 -*-

def get_prompt(title: str, count: int = 8) -> str:
    return f"""
너의 임무: 아래 스키마에 맞는 JSON만 출력해.
- 중복 금지
- 띄어쓰기 금지 (예: #사무실철거)
- 너무 일반적인 단어(#일상 #좋아요) 금지
- 광고/과장 표현 금지
- title에 있는 핵심 단어(지역/업종/작업키워드)는 2~4개 이상 포함
- 총 {count}개로 맞춰줘

입력 제목:
{title}

반환형식(JSON):
{{
  "tags": ["해시태그1", "해시태그2", "해시태그3", "..."]
}}
""".strip()
